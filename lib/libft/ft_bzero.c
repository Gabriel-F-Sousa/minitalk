#include "libft.h"

void	ft_bzero(void *s, size_t len)
{
	int		n;
	int		size;
	char	*cha;

	n = 0;
	size = len;
	cha = s;
	while (n < size)
	{
		cha[n] = '\0';
		n++;
	}
}
